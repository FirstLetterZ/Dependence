package com.zpf.views;

public class TypeTextInfo {
    public  int type;
    public  int id;
    public  int color;
    public  String content;

    public TypeTextInfo() {
    }

    public TypeTextInfo(int type, int id, int color, String content) {
        this.type = type;
        this.id = id;
        this.color = color;
        this.content = content;
    }
}
